class Operacao:

    def __init__(self, soma, produto):
        self.soma = soma
        self.produto = produto
        

    def soma(valor1, valor2, valor3):
        s = valor1 + valor2 + valor3
        return s

    def produto(valor1, valor2, valor3):
        p = valor1 * valor2 * valor3
        return p
        